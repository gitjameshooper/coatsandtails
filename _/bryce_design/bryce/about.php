<p class="body">&quot;Dishongh&quot; is pronounced &quot;dish-on.&quot; It's French. I'm not very French, though. I was raised in Houston and grew up reading a lot and drawing even more. I decided to pursue a master's degree in Technical Communication because it requires a keen eye for detail and I liked using pictures to communicate concepts. I graduated in 2007 and was quite grateful to have been immediately hired as a technical writer and graphic designer for a software company. After polishing my design skills, I decided I wanted to focus more on design and illustration, so I did that freelance for a year. But alas I could not turn down the opportunity to work for<a class="body" href="http://www.youtube.com/watch?v=cBqABsX4KcE" target="_blank">a truly great company</a> (that's me doing the type on the white board) as a technical writer, so I did that for the next two years.</p>
<p align="center" class="body">That pretty much brings us up to speed. On the side, I'm exercising my design and illustration skills to make<a class="body" href="http://www.coatandtails.com" target="_blank">dog portraits</a>and help raise money for local rescue groups. Not on the side, I'm looking to be a part of an organization where I can make gnarly images all day long.</p>
